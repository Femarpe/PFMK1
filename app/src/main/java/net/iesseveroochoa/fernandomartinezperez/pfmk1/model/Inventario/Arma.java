package net.iesseveroochoa.fernandomartinezperez.pfmk1.model.Inventario;

public class Arma {
    private String nombre;

    private String bono;

    private String danyo;

    private String tipodanyo;

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getBono() {
        return bono;
    }

    public void setBono(String bono) {
        this.bono = bono;
    }

    public String getDanyo() {
        return danyo;
    }

    public void setDanyo(String danyo) {
        this.danyo = danyo;
    }

    public String getTipodanyo() {
        return tipodanyo;
    }

    public void setTipodanyo(String tipodanyo) {
        this.tipodanyo = tipodanyo;
    }
}
